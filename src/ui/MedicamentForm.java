    public void load(){
        model.setRowCount(0);
        for(Medicament m : ms.findAll()){
            Fournisseur f=fs.findById(m.getIdFournisseur());
            model.addRow(new Object[]{
                m.getId(),
                m.getNom(),
                m.getFamille(),
                m.getPrix(),
                m.getStock(),
                f
            });
        }
    }
