    private void listMedMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listMedMouseClicked
        int selectedRow = listMed.getSelectedRow();

    if (selectedRow >= 0) {
        // Assuming columns: 0:id, 1:nom, 2:famille, 3:prix, 4:quantite, 5:fournisseur
        id = Integer.parseInt(listMed.getModel().getValueAt(selectedRow, 0).toString());
        String nom = listMed.getModel().getValueAt(selectedRow, 1).toString();
        String famille = listMed.getModel().getValueAt(selectedRow, 2).toString();
        String prix = listMed.getModel().getValueAt(selectedRow, 3).toString();
        String quantite = listMed.getModel().getValueAt(selectedRow, 4).toString();
        String fournisseurNameOrId = listMed.getModel().getValueAt(selectedRow, 5).toString();

        nameTxt.setText(nom);
        familleTxt.setText(famille);
        prixDouble.setText(prix);
        stockInt.setText(quantite);

        
    }
    }//GEN-LAST:event_listMedMouseClicked

    private void suppMedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suppMedActionPerformed
        int reponse=JOptionPane.showConfirmDialog(this, "voulez vous supprimer ce medicament ?");
        if(reponse==0){
            ms.delete(ms.findById(id));
            load();
            vider();
        }
    }//GEN-LAST:event_suppMedActionPerformed

    private void UpdateMedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateMedActionPerformed
            int reponse = JOptionPane.showConfirmDialog(this, "Voulez-vous modifier ce médicament ?");
            if (reponse == JOptionPane.YES_OPTION) {  // or == 0, but YES_OPTION is clearer

                try {
                    // Récupérer les valeurs du formulaire
                    double prix = Double.parseDouble(prixDouble.getText());
                    int stock = Integer.parseInt(stockInt.getText());
                    Fournisseur selectedFournisseur = (Fournisseur) comboFournisseur.getSelectedItem();
                    if (selectedFournisseur == null) {
                        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un fournisseur.", "Erreur", JOptionPane.ERROR_MESSAGE);
                        return;
                    }

                    Medicament medicament = new Medicament(
                        id,                           // il faut que tu conserves cet id (champ global ou autre)
                        nameTxt.getText(),
                        familleTxt.getText(),
                        prix,
                        stock,
                        selectedFournisseur.getId()
                    );

                    ms.update(medicament);  // appel à ta méthode update dans MedicamentService

                    load();   // recharge la liste/tableau des médicaments
                    vider();  // vide le formulaire

                    JOptionPane.showMessageDialog(this, "Médicament modifié avec succès.");

                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Prix ou stock invalide.", "Erreur", JOptionPane.ERROR_MESSAGE);
                }
            }
    }//GEN-LAST:event_UpdateMedActionPerformed
    public void loadFournisseurs() {
        List<Fournisseur> fournisseurs = fs.findAll();
        comboFournisseur.removeAllItems();
        for (Fournisseur f : fournisseurs) {
            comboFournisseur.addItem(f);
        }
    }

