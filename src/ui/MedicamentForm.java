    private void ajoutMedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajoutMedActionPerformed
        String nom = nameTxt.getText();
        String famille = familleTxt.getText();
        double prix = Double.parseDouble(prixDouble.getText());
        int stock = Integer.parseInt(stockInt.getText());

        // Récupérer le fournisseur sélectionné
        Fournisseur selectedFournisseur = (Fournisseur) comboFournisseur.getSelectedItem();
        Integer idFournisseur = null;
        if (selectedFournisseur != null) {
            idFournisseur = selectedFournisseur.getId();
        } else {
            // gérer le cas où aucun fournisseur n'est sélectionné
            System.out.println("Veuillez sélectionner un fournisseur.");
            return;
        }

        Medicament medicament = new Medicament(nom, famille, prix, stock, idFournisseur);
        ms.create(medicament);
        load();
        vider();
    }//GEN-LAST:event_ajoutMedActionPerformed

